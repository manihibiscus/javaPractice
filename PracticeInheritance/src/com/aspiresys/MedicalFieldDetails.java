package com.aspiresys;

public class MedicalFieldDetails {
	String medicalFieldName;
	String doctorName;
	String experiance;
	int licenseNO;
	public MedicalFieldDetails(String medicalFieldName, String doctorName, String experiance, int licenseNO) {
		super();
		this.medicalFieldName = medicalFieldName;
		this.doctorName = doctorName;
		this.experiance = experiance;
		this.licenseNO = licenseNO;
	}
	
}
